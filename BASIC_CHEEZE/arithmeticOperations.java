package Basic_Cheeze;

public class arithmeticOperations {
    static void main() {
        int x = 5 , y = 2;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);


        double z = 5 , n = 2;
        System.out.println(z+n);
        System.out.println(z-n);
        System.out.println(z/n);
        System.out.println(z*n);
    }
}
